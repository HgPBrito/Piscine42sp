#include "header.h"

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char *r;

	r = dest;
	while (*dest != '\0')
		dest++;
	while ((*dest++ = *src++) != '\0' && nb-- > 0)
		;
	*--dest = '\0';
	return (r);
}
