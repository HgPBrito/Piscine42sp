int		ft_str_is_printable(char *str)
{
	unsigned int	cont;

	cont = 0;
	while (str[cont] != '\0')
	{
		if ((str[cont] < 32) || (str[cont] == 127))
		{
			return (0);
		}
		cont++;
	}
	return (1);
}
