void	ft_4_middle(int **square)
{
	if (square[1][1] == '0' && square[0][1] != '4' && square[2][1] != '4' &&
	square[3][1] != '4' && square[1][0] != '4' &&
	square[1][2] != '4' && square[1][3] != '4')
		square[1][1] = '4';
	if (square[1][2] == '0' && square[0][2] != '4' && square[2][2] != '4' &&
	square[3][2] != '4' && square[1][0] != '4' &&
	square[1][1] != '4' && square[1][3] != '4')
		square[1][2] = '4';
	if (square[2][1] == '0' && square[0][1] != '4' && square[1][1] != '4' &&
	square[3][1] != '4' && square[2][0] != '4' &&
	square[2][2] != '4' && square[2][3] != '4')
		square[2][1] = '4';
	if (square[2][2] == '0' && square[0][2] != '4' && square[1][2] != '4' &&
	square[3][2] != '4' && square[2][0] != '4' &&
	square[2][1] != '4' && square[2][3] != '4')
		square[2][2] = '4';
}
