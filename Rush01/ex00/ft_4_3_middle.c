void	ft_4_3_middle(int **square)
{
	if ((square[3][1] == '4' && square[3][2] == '3') &&
		(square[2][3] == '1' || square[1][3] == '1' || square[0][3] == '1'))
		square[3][3] = '2';
	if ((square[1][3] == '4' && square[2][3] == '3') &&
		(square[3][2] == '1' || square[3][1] == '1' || square[3][0] == '1'))
		square[3][3] = '2';
	if ((square[3][1] == '3' && square[3][2] == '4') &&
		(square[2][0] == '1' || square[1][0] == '1' || square[0][0] == '1'))
		square[3][0] = '2';
	if ((square[1][3] == '3' && square[2][3] == '4') &&
		(square[0][2] == '1' || square[0][1] == '1' || square[0][0] == '1'))
		square[0][3] = '2';
	if ((square[0][1] == '3' && square[0][2] == '4') &&
		(square[1][0] == '1' || square[2][0] == '1' || square[3][0] == '1'))
		square[0][0] = '2';
	if ((square[1][0] == '3' && square[2][0] == '4') &&
		(square[0][1] == '1' || square[0][2] == '1' || square[0][3] == '1'))
		square[0][0] = '2';
	if ((square[0][1] == '4' && square[0][2] == '3') &&
		(square[1][3] == '1' || square[2][3] == '1' || square[3][3] == '1'))
		square[0][3] = '2';
	if ((square[1][0] == '4' && square[2][0] == '3') &&
		(square[3][1] == '1' || square[3][2] == '1' || square[3][3] == '1'))
		square[3][0] = '2';
}
